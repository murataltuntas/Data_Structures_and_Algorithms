    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        if (evt.getSource() == jComboBox1) {
            JComboBox cb = (JComboBox)evt.getSource();
            String teamN = (String)cb.getSelectedItem();
            
            jTextArea1.setText("");
            for (int i = 0; i < federation.searchTeam(teamN).getMatchScheduleAYear().size(); i++) {
                jTextArea1.append((i + 1) + ". Week\t" 
                        + federation.searchTeam(teamN).getMatchScheduleAYear().get(i) + "\n");
            }
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        PrintWriter writer = null;
        try {
            writer = new PrintWriter(jTextField1.getText(), "UTF-8");
            writer.println(jTextArea1.getText());
            writer.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FederationPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (UnsupportedEncodingException ex) {
            Logger.getLogger(FederationPanel.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            writer.close();
            jTextField1.setText("");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    public String[] takimlar(){
        String [] tms =null;
        
        if(federation.getTeams().size() > 0)
            tms = new String[federation.getTeams().size()];
        
        for (int i = 0; i < federation.getTeams().size(); i++) {
            tms[i] = federation.getTeams().get(i).getName();
        }
        return tms;
    }
    
