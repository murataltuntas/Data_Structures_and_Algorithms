    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int scr = 0;
        try{
            scr = Integer.parseInt(jTextField2.getText());
        }
        catch(NumberFormatException e){
            scr = 0;
        }
        finally{
            jTextArea1.setText("");
            jTextArea1.append("Team\t\tScore\n\n");
            for (int i = 0; i < federation.scoresWeekly(scr).size(); i++) {
                jTextArea1.append((i+1) + ". ");
                jTextArea1.append(federation.scoresWeekly(scr).get(i));
                jTextArea1.append("\n");
            }
        }
    
    }//GEN-LAST:event_jButton3ActionPerformed

